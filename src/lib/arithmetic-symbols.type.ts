/**
 * @description A collection of common arithmetic symbols.
 * @export
 */
export type ArithmeticSymbols =
  | '%'  // Modulo operator
  | '*'  // Asterisk (U+002A)
  | '+'  // Addition, Plus (U+002B)
  | '/'  // Slash (division) (U+002F)
  | '='  // Equals (U+003D)
  | '±'  // Plus-minus (U+00B1)
  | '×'  // Multiplication (U+00D7)
  | '÷'  // Division (U+00F7)
  | '−'  // Subtraction, Minus (U+2212)
  | '∓'  // Minus-plus (U+2213)
  | '≈'  // Approximately equal to (U+2248)
  | '≠'  // Not equal to (U+2260)
  | '≡'  // Identical to (U+2261)
;

/**
 * @description A collection of common algebraic symbols.
 * @export
 */
export type AlgebraicSymbols =
  | '∂'  // Partial derivative
  | '∆'  // Delta
  | '∇'  // Nabla (gradient)
  | '∏'  // Product notation
  | '∑'  // Summation
  | '√'  // Square root (U+221A)
  | '∛'  // Cube root (U+221B)
  | '∜'  // Fourth root
  | '∝'   // Proportional to (U+221D)
  | '∞'  // Infinity
  | '⊕'   // Direct sum (U+2295)
  | '⊗'   // Tensor product (U+2297)
;
